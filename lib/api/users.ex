  @doc """
  Resets the user password.

  ## Examples

      iex> reset_user_password(user, %{password: "new long password", password_confirmation: "new long password"})
      {:ok, %User{}}

      iex> reset_user_password(user, %{password: "valid", password_confirmation: "not the same"})
      {:error, %Ecto.Changeset{}}

  """
  def reset_user_password(user, attrs) do
    Ecto.Multi.new()
    |> Ecto.Multi.update(:user, User.password_changeset(user, attrs))
    |> Ecto.Multi.delete_all(:tokens, UserToken.user_and_contexts_query(user, :all))
    |> Repo.transaction()
    |> case do
      {:ok, %{user: user}} -> {:ok, user}
      {:error, :user, changeset, _} -> {:error, changeset}
    end
  end

  ## User registration

  @doc """
  Registers a user for ueberauth if not exists.

  ## Examples

      iex> find_or_create(%{field: value})
      {:ok, %User{}}

      iex> find_or_create(%{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def find_or_create(%Ueberauth.Auth{} = auth) do
    password = Dictionary.random_word() <> "_" <> Dictionary.random_word()

    user_to_register =
      case auth.provider do
        :shift4shop ->
          %{
            email: "",
            terms: "on",
            confirmed_at: DateTime.utc_now(),
            password: password,
            password_confirmation: password,
            shift4shop_uid: auth.uid
          }

        :github ->
          %{
            email: "",
            terms: "on",
            confirmed_at: DateTime.utc_now(),
            password: password,
            password_confirmation: password,
            github_uid: auth.uid
          }
      end

    case get_user_by_uid(auth.uid, auth.provider) do
      nil ->
        oauth_register_user(user_to_register)

      {:ok, nil} ->
        oauth_register_user(user_to_register)

      data ->
        data
    end
  end

  def find_or_create(auth) do
    case auth.provider do
      :shift4shop ->
        case get_user_by_uid(auth.uid, :shift4shop) do
          nil ->
            {_, user} = oauth_register_user(auth)
            User.update_user(user, %{shift4shop_uid: auth.uid})
          reply ->
            {:ok,reply}
        end

      :github ->
        case get_user_by_uid(auth.uid, :github) do
          nil ->
            user = oauth_register_user(auth)
            User.update_user(user, %{github_uid: auth.uid})

          reply ->
            reply
        end
    end
  end

  def token(auth) do
    case auth.provider do
      :shift4shop -> %{shift4shop_token: auth.credentials.token}
      :github -> %{github_token: auth.credentials.token}
    end
  end

